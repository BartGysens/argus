<h2>Wat is het SODA-project?</h2>
<p>
	<strong>Vzw SODAjobs</strong> begeleidt scholen om op een <strong><a
		href="http://www.sodajobs.be/over/info-scholen/onderzoek-ugent-effici-euml-ntie-soda" target="_blank">wetenschappelijke</a> manier
		efficiënter te werken aan gedragsverandering</strong>. Dit doen we
	door leerlingen te <strong>belonen</strong>, die uitblinken in <strong>Stiptheid</strong>,
	<strong>Orde</strong>, <strong>Discipline</strong>, en een <strong>professionele
		Attitude</strong> hebben.
</p>
<p>
	Als vzw werken we aan de <strong>kwaliteit van de SODA-scholen</strong>
	door te luisteren naar <strong>feedback </strong>van de <strong>SODA-leerlingen</strong>,
	de <strong>werkgevers </strong>en de <strong>SODA-scholen</strong>
	zelf. Op basis van deze informatie kunnen we onze <strong>scholen
		doelgerichter begeleiden</strong> &amp; <a
		href="http://www.sodajobs.be/over/info-scholen/faq-soda-project" target="_blank">coachen</a>. Dit is
	noodzakelijk om <strong>zoveel mogelijk leerlingen te
		stimuleren/motiveren</strong>.
</p>
<p>
	Voor ons het belangrijk om het <strong>evalueren efficiënter</strong> (<strong>minder
		planlast</strong>) te maken zodoende meer tijd te hebben voor een <strong>effectievere
		feedback</strong> naar de leerling toe.
</p>
<p>
	Via verscheidene netwerkevents werken we aan de <strong>brug tussen
		werkgevers en scholen</strong>.
</p>
<p>
	De <strong>beloning </strong>(zelf) bestaat uit een grote keuze aan
	leuke/leerrijke <a href="http://www.sodajobs.be/jobs" target="_blank"><strong>vakantiejobs</strong>, <strong>stageplaatsen</strong>,
		weekendwerk, vaste jobs</a> en <strong><a
		href="https://www.facebook.com/sodaupstream" target="_blank">gratis events voor
			SODA-leerlingen</a></strong>.
</p>
<p>
	De bedoeling is een <strong>positieve attitude</strong> in het TSO en
	BSO te <strong>stimuleren </strong>en op die manier het <strong>niveau
		te verhogen</strong>. Een tweede doel is om (allochtone/kansarme) <strong>leerlingen
		een extra objectief selectiecriterium</strong> aan te bieden.
</p>
<p>
	Als vzw willen wij dat <strong>SODA-scholen meer</strong> kunnen <strong>opvallen
		met hun “goede leerlingen”</strong> en zo komaf maken met de negatieve
	perceptie die ontstaat door het watervalsysteem.
</p>
<p>
	<a href="http://www.sodajobs.be/over/info-bedrijven" target="_blank"><strong>Werkgevers </strong></a>investeren
	jaarlijks veel <strong>tijd en geld</strong> aan de <strong>zoektocht
		naar professionele werkkrachten</strong>. In de eerste plaats is het
	te danken aan ons onderwijs dat deze werknemers op de arbeidsmarkt
	terecht komen. Via vzw SODAjobs kan een deel van dat <strong>geld
		terugvloeien naar het onderwijs</strong>.
</p>
